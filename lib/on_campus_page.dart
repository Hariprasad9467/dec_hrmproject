import 'dart:convert';
import 'package:flutter/material.dart';
import 'package:intl/intl.dart';

import '../sidebar.dart';
import '../services/oncampus_service.dart';
import '../student_details_page.dart';

class OnCampusPage extends StatefulWidget {
  const OnCampusPage({super.key});

  @override
  State<OnCampusPage> createState() => _OnCampusPageState();
}

class _OnCampusPageState extends State<OnCampusPage> {
  final DateFormat _dateFmt = DateFormat('yyyy-MM-dd');

  List<Map<String, dynamic>> _drives = [];
  List<Map<String, dynamic>> _filteredDrives = [];
  final TextEditingController _searchController = TextEditingController();
  bool _loading = true;

  // Dropdown options
  final List<String> _bgOptions = ['Pending', 'In Progress', 'Verified', 'Unable to Verify'];
  final List<String> _positionOptions = ['Intern', 'Tech Trainee'];

  @override
  void initState() {
    super.initState();
    _loadDrives();
    _searchController.addListener(_onSearchChanged);
  }

  @override
  void dispose() {
    _searchController.removeListener(_onSearchChanged);
    _searchController.dispose();
    super.dispose();
  }

  void _onSearchChanged() {
    final q = _searchController.text.trim().toLowerCase();
    setState(() {
      if (q.isEmpty) {
        _filteredDrives = List.from(_drives);
      } else {
        _filteredDrives = _drives.where((d) {
          final combined = (
                (d['collegeName'] ?? '') +
                // ignore: prefer_interpolation_to_compose_strings
                ' ' +
                (d['selectedPosition'] ?? '') +
                // ignore: prefer_interpolation_to_compose_strings
                ' ' +
                (d['contactPerson'] ?? '') +
                ' ' +
                (d['studentContactDetails'] ?? '')
              ).toLowerCase();
          final dateStr = d['dateOfRecruitment'] != null
              ? _dateFmt.format(DateTime.parse(d['dateOfRecruitment']))
              : '';
          return combined.contains(q) || dateStr.contains(q);
        }).toList();
      }
    });
  }

  Future<void> _loadDrives() async {
    setState(() => _loading = true);
    try {
      final data = await OnCampusService.fetchDrives();
      setState(() {
        _drives = data.map<Map<String, dynamic>>((e) => Map<String, dynamic>.from(e)).toList();
        _filteredDrives = List.from(_drives);
      });
    } catch (e) {
      debugPrint('load drives error: $e');
      ScaffoldMessenger.of(context).showSnackBar(const SnackBar(content: Text('Failed to load drives')));
    } finally {
      setState(() => _loading = false);
    }
  }

  Future<void> _createOrUpdateDrive(Map<String, dynamic> payload, {String? id}) async {
    try {
      if (id == null) {
        await OnCampusService.createDrive(payload);
      } else {
        await OnCampusService.updateDrive(id, payload);
      }
      await _loadDrives();
    } catch (e) {
      debugPrint('save drive error: $e');
      ScaffoldMessenger.of(context).showSnackBar(const SnackBar(content: Text('Failed to save drive')));
    }
  }

  Future<void> _confirmDelete(String id) async {
    final ok = await showDialog<bool>(
      context: context,
      builder: (ctx) => AlertDialog(
        title: const Text('Delete Drive'),
        content: const Text('Are you sure you want to delete this record? This action cannot be undone.'),
        actions: [
          TextButton(onPressed: () => Navigator.pop(ctx, false), child: const Text('Cancel')),
          ElevatedButton(onPressed: () => Navigator.pop(ctx, true), child: const Text('Delete')),
        ],
      ),
    );
    if (ok == true) {
      try {
        await OnCampusService.deleteDrive(id);
        await _loadDrives();
        ScaffoldMessenger.of(context).showSnackBar(const SnackBar(content: Text('Drive deleted')));
      } catch (e) {
        debugPrint('delete drive error: $e');
        ScaffoldMessenger.of(context).showSnackBar(const SnackBar(content: Text('Failed to delete')));
      }
    }
  }

  Future<void> _exportPdf(String driveId) async {
    try {
      final resp = await OnCampusService.exportDrivePdf(driveId);
      if (resp.statusCode == 200) {
        final url = '${OnCampusService.baseUrl}/api/oncampus/$driveId/export';
        showDialog(
          context: context,
          builder: (ctx) => AlertDialog(
            title: const Text('Export PDF'),
            content: SelectableText('Download from: $url'),
            actions: [TextButton(onPressed: () => Navigator.pop(ctx), child: const Text('Close'))],
          ),
        );
      } else {
        ScaffoldMessenger.of(context).showSnackBar(const SnackBar(content: Text('Export failed')));
      }
    } catch (e) {
      debugPrint('export error: $e');
      ScaffoldMessenger.of(context).showSnackBar(const SnackBar(content: Text('Export failed')));
    }
  }

  Future<void> _showAddOrEditDialog({Map<String, dynamic>? existing}) async {
    final isEdit = existing != null;
    DateTime? chosenDate = existing != null ? DateTime.tryParse(existing['dateOfRecruitment'] ?? '') : null;
    final dateController = TextEditingController(text: chosenDate != null ? _dateFmt.format(chosenDate) : '');

    final collegeController = TextEditingController(text: existing?['collegeName'] ?? '');
    final totalStudentsController = TextEditingController(text: existing?['totalStudents']?.toString() ?? '');
    final aptitudeController = TextEditingController(text: existing?['aptitudeSelected']?.toString() ?? '');
    final techController = TextEditingController(text: existing?['techSelected']?.toString() ?? '');
    final hrController = TextEditingController(text: existing?['hrSelected']?.toString() ?? '');

    String bgValue = existing?['bgVerificationStatus']?.toString() ?? _bgOptions.first;
    String positionValue = existing?['selectedPosition']?.toString() ?? _positionOptions.first;
    final contactPersonController = TextEditingController(text: existing?['contactPerson'] ?? '');
    final studentContactController = TextEditingController(text: existing?['studentContactDetails'] ?? '');

    String? errorText;

    await showDialog(
      context: context,
      builder: (ctx) {
        return StatefulBuilder(builder: (ctx2, setStateDialog) {
          Future<void> pickDate() async {
            final now = DateTime.now();
            final picked = await showDatePicker(
              context: ctx2,
              initialDate: chosenDate ?? now,
              firstDate: DateTime(2000),
              lastDate: DateTime(2100),
            );
            if (picked != null) {
              chosenDate = picked;
              dateController.text = _dateFmt.format(picked);
              setStateDialog(() {});
            }
          }

          return AlertDialog(
            title: Text(isEdit ? 'Edit Drive' : 'Add Drive'),
            content: SingleChildScrollView(
              child: Column(
                mainAxisSize: MainAxisSize.min,
                children: [
                  TextField(controller: dateController, readOnly: true, onTap: pickDate, decoration: const InputDecoration(labelText: 'Date of Recruitment', suffixIcon: Icon(Icons.calendar_today))),
                  const SizedBox(height: 8),
                  TextField(controller: collegeController, decoration: const InputDecoration(labelText: 'College Name')),
                  const SizedBox(height: 8),
                  TextField(controller: totalStudentsController, keyboardType: TextInputType.number, decoration: const InputDecoration(labelText: 'Total Students (Count)')),
                  const SizedBox(height: 8),
                  TextField(controller: aptitudeController, keyboardType: TextInputType.number, decoration: const InputDecoration(labelText: 'Aptitude Selected (Count)')),
                  const SizedBox(height: 8),
                  TextField(controller: techController, keyboardType: TextInputType.number, decoration: const InputDecoration(labelText: 'Tech Selected (Count)')),
                  const SizedBox(height: 8),
                  TextField(controller: hrController, keyboardType: TextInputType.number, decoration: const InputDecoration(labelText: 'HR Selected (Count)')),
                  const SizedBox(height: 8),
                  DropdownButtonFormField<String>(initialValue: bgValue, items: _bgOptions.map((s) => DropdownMenuItem(value: s, child: Text(s))).toList(), onChanged: (v) => setStateDialog(() => bgValue = v ?? _bgOptions.first), decoration: const InputDecoration(labelText: 'BG Verification Status')),
                  const SizedBox(height: 8),
                  DropdownButtonFormField<String>(initialValue: positionValue, items: _positionOptions.map((s) => DropdownMenuItem(value: s, child: Text(s))).toList(), onChanged: (v) => setStateDialog(() => positionValue = v ?? _positionOptions.first), decoration: const InputDecoration(labelText: 'Selected Position')),
                  const SizedBox(height: 8),
                  TextField(controller: contactPersonController, decoration: const InputDecoration(labelText: 'Contact Person')),
                  const SizedBox(height: 8),
                  TextField(controller: studentContactController, decoration: const InputDecoration(labelText: 'Student Contact details (CSV or notes)')),
                  if (errorText != null) ...[const SizedBox(height: 8), Text(errorText!, style: const TextStyle(color: Colors.red))],
                ],
              ),
            ),
            actions: [
              TextButton(onPressed: () => Navigator.pop(ctx2), child: const Text('Cancel')),
              ElevatedButton(
                onPressed: () async {
                  if (dateController.text.trim().isEmpty) {
                    setStateDialog(() => errorText = 'Please pick a date.');
                    return;
                  }
                  if (collegeController.text.trim().isEmpty) {
                    setStateDialog(() => errorText = 'Please enter college name.');
                    return;
                  }

                  int parseOrZero(String s) {
                    try { return int.parse(s.trim()); } catch (_) { return 0; }
                  }

                  final payload = <String, dynamic>{
                    'dateOfRecruitment': chosenDate!.toIso8601String(),
                    'collegeName': collegeController.text.trim(),
                    'totalStudents': parseOrZero(totalStudentsController.text),
                    'aptitudeSelected': parseOrZero(aptitudeController.text),
                    'techSelected': parseOrZero(techController.text),
                    'hrSelected': parseOrZero(hrController.text),
                    'bgVerificationStatus': bgValue,
                    'selectedPosition': positionValue,
                    'contactPerson': contactPersonController.text.trim(),
                    'studentContactDetails': studentContactController.text.trim(),
                  };

                  try {
                    if (isEdit) {
                      final id = existing['_id'] ?? existing['id'];
                      await _createOrUpdateDrive(payload, id: id);
                    } else {
                      await _createOrUpdateDrive(payload);
                    }
                    Navigator.pop(ctx2);
                  } catch (e) {
                    setStateDialog(() => errorText = 'Save failed');
                  }
                },
                child: Text(isEdit ? 'Save' : 'Add'),
              ),
            ],
          );
        });
      },
    );
  }

  Widget _buildTable() {
    if (_filteredDrives.isEmpty) {
      return const Center(child: Padding(padding: EdgeInsets.all(24.0), child: Text('No drives yet. Tap + to add a drive.')));
    }

    final rows = _filteredDrives.map((row) {
      final id = (row['_id'] ?? row['id'])?.toString() ?? '';
      return DataRow(cells: [
        DataCell(Text(_dateFmt.format(DateTime.parse(row['dateOfRecruitment']))), onTap: () => _showAddOrEditDialog(existing: row)),
        DataCell(Text(row['collegeName'] ?? ''), onTap: () => _showAddOrEditDialog(existing: row)),
        DataCell(Text((row['totalStudents'] ?? 0).toString()), onTap: () => _showAddOrEditDialog(existing: row)),
        DataCell(Text((row['aptitudeSelected'] ?? 0).toString()), onTap: () => _showAddOrEditDialog(existing: row)),
        DataCell(Text((row['techSelected'] ?? 0).toString()), onTap: () => _showAddOrEditDialog(existing: row)),
        DataCell(Text((row['hrSelected'] ?? 0).toString()), onTap: () => _showAddOrEditDialog(existing: row)),
        DataCell(Text(row['bgVerificationStatus']?.toString() ?? 'Pending'), onTap: () => _showAddOrEditDialog(existing: row)),
        DataCell(Text(row['selectedPosition'] ?? ''), onTap: () => _showAddOrEditDialog(existing: row)),
        DataCell(Text(row['contactPerson'] ?? ''), onTap: () => _showAddOrEditDialog(existing: row)),
        DataCell(Text(row['studentContactDetails'] ?? ''), onTap: () => _showAddOrEditDialog(existing: row)),
        DataCell(Row(children: [
          IconButton(icon: const Icon(Icons.remove_red_eye, color: Colors.green), onPressed: () {
            Navigator.push(context, MaterialPageRoute(builder: (_) => StudentDetailsPage(driveId: id)));
          }),
          IconButton(icon: const Icon(Icons.picture_as_pdf, color: Colors.deepPurple), onPressed: () => _exportPdf(id)),
          IconButton(icon: const Icon(Icons.edit, color: Colors.blue), onPressed: () => _showAddOrEditDialog(existing: row)),
          IconButton(icon: const Icon(Icons.delete, color: Colors.red), onPressed: () => _confirmDelete(id)),
        ])),
      ]);
    }).toList();

    return SingleChildScrollView(
      scrollDirection: Axis.horizontal,
      child: ConstrainedBox(
        constraints: BoxConstraints(minWidth: MediaQuery.of(context).size.width),
        child: DataTable(
          columnSpacing: 16,
          horizontalMargin: 12,
          columns: const [
            DataColumn(label: Text('Date')),
            DataColumn(label: Text('College Name')),
            DataColumn(label: Text('Total Students')),
            DataColumn(label: Text('Aptitude Selected')),
            DataColumn(label: Text('Tech Selected')),
            DataColumn(label: Text('HR Selected')),
            DataColumn(label: Text('BG Verification')),
            DataColumn(label: Text('Selected Position')),
            DataColumn(label: Text('Contact Person')),
            DataColumn(label: Text('Student Contact Details')),
            DataColumn(label: Text('Actions')),
          ],
          rows: rows,
        ),
      ),
    );
  }

  @override
  Widget build(BuildContext context) {
    final totalCompleted = _drives.length;
    return Sidebar(
      title: 'On Campus Recruitment',
      body: SizedBox(
        width: double.infinity,
        child: Column(
          children: [
            Padding(
              padding: const EdgeInsets.all(20),
              child: Row(
                children: [
                  Expanded(child: Text('Total no.of Completed Drives: $totalCompleted', style: const TextStyle(fontSize: 18, fontWeight: FontWeight.bold, color: Colors.white))),
                  SizedBox(width: 300, child: TextField(controller: _searchController, decoration: const InputDecoration(filled: true, fillColor: Colors.white, prefixIcon: Icon(Icons.search), hintText: 'Search college / position / contact / date', border: OutlineInputBorder(), isDense: true))),
                  const SizedBox(width: 12),
                  ElevatedButton.icon(onPressed: () => _showAddOrEditDialog(), icon: const Icon(Icons.add), label: const Text('Add Drive'), style: ElevatedButton.styleFrom(backgroundColor: Colors.white, foregroundColor: Colors.black)),
                  const SizedBox(width: 12),
                  ElevatedButton(onPressed: _loadDrives, style: ElevatedButton.styleFrom(backgroundColor: Colors.white, foregroundColor: Colors.black), child: const Text('Refresh')),
                  const SizedBox(width: 12),
                  ElevatedButton(onPressed: () {
                    showDialog(context: context, builder: (_) => AlertDialog(title: const Text('Menu'), content: const Text('Menu options (add your items)'), actions: [TextButton(onPressed: () => Navigator.pop(context), child: const Text('Close'))]));
                  }, style: ElevatedButton.styleFrom(backgroundColor: Colors.white, foregroundColor: Colors.black, shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(50)), padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 12)), child: const Text('Menu')),
                ],
              ),
            ),

            Expanded(child: _loading ? const Center(child: CircularProgressIndicator()) : Padding(padding: const EdgeInsets.symmetric(horizontal: 12.0), child: Card(elevation: 2, clipBehavior: Clip.hardEdge, child: Padding(padding: const EdgeInsets.all(12.0), child: SingleChildScrollView(child: _buildTable()))))),
            Padding(padding: const EdgeInsets.symmetric(horizontal: 20.0, vertical: 8), child: Row(mainAxisAlignment: MainAxisAlignment.end, children: [IconButton(icon: const Icon(Icons.download, color: Colors.white), onPressed: () {
              final jsonText = jsonEncode(_drives);
              showDialog(context: context, builder: (ctx) => AlertDialog(title: const Text('Export JSON'), content: SizedBox(width: double.maxFinite, child: SingleChildScrollView(child: Text(jsonText))), actions: [TextButton(onPressed: () => Navigator.pop(ctx), child: const Text('Close'))]));
            })])),
          ],
        ),
      ),
    );
  }
}
